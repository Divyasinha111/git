// Logical AND (&&) example
let isTrue1 = true;
let isTrue2 = true;
let resultAnd = isTrue1 && isTrue2;
console.log(`Logical AND Result: ${resultAnd}`); // Output: Logical AND Result: true

// Logical OR (||) example
let isTrue3 = true;
let isFalse = false;
let resultOr = isTrue3 || isFalse;
console.log(`Logical OR Result: ${resultOr}`); // Output: Logical OR Result: true

// Logical NOT (!) example
let isTrue4 = true;
let resultNot = !isTrue4;
console.log(`Logical NOT Result: ${resultNot}`); // Output: Logical NOT Result: false



